package com.freeui.player;

public class DemoQueueData {
    String uri;
    String trackName;
    String artist;
    String thumburi;

    public DemoQueueData(String trackName, String artist, String thumburi, String uri) {
        this.trackName = trackName;
        this.artist = artist;
        this.thumburi = thumburi;
        this.uri = uri;
    }
}

